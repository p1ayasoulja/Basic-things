public class Learn {
    static void Lear(int l[]) {
        System.out.println(l.length);

        for (int x : l) {
            System.out.print(x + " ");
        }
        System.out.println();
    }

    public static void main(String[] args) {
        int n[] = {5, 3, 2};
        Lear(n);

    }
}

